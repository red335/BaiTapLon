CREATE TABLE TAIKHOAN(
  MATAIKHOAN INT IDENTITY PRIMARY KEY,
  USERNAME NVARCHAR(30),
  PASS NVARCHAR(30),
  DISLAY_NAME NVARCHAR(30), -- TEN HIEN THI
  NGAY_LAP DATE DEFAULT(GETDATE()),
  LOAI_TAI_KHOAN NVARCHAR(30), -- GAN GIONG VOI LOAI NHAN VIEN, LOAI TAI KHOAN BAO GOM: NHAN VIEN MUA BAN, NHAN VIEN SUA CHUA, ADMIN, KHACH HANG   
  AVATAR NVARCHAR(100) DEFAULT(N'Img/TaiKhoan/Default.jpg')
)
CREATE TABLE QUYEN_CHA(
  MAQUYEN INT IDENTITY PRIMARY KEY,
  TENQUYEN NVARCHAR(30) ,
)

CREATE TABLE QUYEN_CON(
  MAQUYEN INT IDENTITY PRIMARY KEY,
  MAQUYEN_CHA INT FOREIGN KEY (MAQUYEN_CHA)REFERENCES QUYEN_CHA (MAQUYEN) ON  DELETE CASCADE  ON UPDATE CASCADE, 
  TENQUYEN NVARCHAR(30)
)
CREATE TABLE HOPTHU_DEN(
  NG_GUI  INT  FOREIGN KEY (NG_GUI) REFERENCES TAIKHOAN( MATAIKHOAN) ON DELETE CASCADE ON UPDATE CASCADE, 
  NG_NHAN INT  FOREIGN KEY (NG_NHAN) REFERENCES TAIKHOAN( MATAIKHOAN), 
  NGAYGUI DATETIME DEFAULT(GETDATE()),
  DAXEM BIT,
  NOIDUNG NVARCHAR,

  CONSTRAINT PK_THUNHAN PRIMARY KEY(NG_GUI,NG_NHAN,NGAYGUI)
