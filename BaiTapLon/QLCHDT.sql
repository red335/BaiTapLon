CREATE TABLE KHUYENMAI_SALEKHUNG(
   MAKM INT FOREIGN KEY (MAKM) REFERENCES KHUYENMAI(MAKM) ON DELETE CASCADE,
   PHANTRAM INT ,
  CONSTRAINT PK_KHUYENMAI_SALEKHUNG PRIMARY KEY  (MAKM)
