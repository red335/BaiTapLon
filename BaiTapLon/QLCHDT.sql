-- TIN NHAN LA THU GIUA CAC TAI KHOAN VOI NHAU, THONG BAO LA TIN TU ADMIN DEN MOI TAI KHOAN
CREATE TABLE THONGBAO(
  MA_THONGBAO INT PRIMARY KEY IDENTITY,
  THONGBAO_NAYLA_CU BIT,
  NOIDUNG NVARCHAR,
)


CREATE TABLE NHOMQUYEN_COBAN(
  LOAI_TK NVARCHAR(30) NOT NULL , 
  MAQUYEN_CON INT FOREIGN KEY (MAQUYEN_CON) REFERENCES QUYEN_CON(MAQUYEN) ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT PK_NHOMQUYEN PRIMARY KEY(LOAI_TK,MAQUYEN_CON)
)
CREATE TABLE HANGSX(
  MAHSX INT IDENTITY PRIMARY KEY,
  TENHANG NVARCHAR(30),
  QUOCGIA NVARCHAR(30)
)
-- CHITIETSANPHAM PHAI DUOC VIET THEO DANG <TEN THUOC TINH 1>:<GIA TRI>;<TEN THUOC TINH 2>:<GIA TRI>;.......
-- VD RAM:4GB;ROM:32GB;MANG HINH:5,6ICNH;CAMERA TRUOC:14MPX;CAMERA SAU:14MPX
-- LUU Y : KHONG DUNG KHOANG CAC O TRUOC VA SAU DAU ':' ,CUNG NHU TRUOC VA SAU DAU ';'
CREATE TABLE SANPHAM(
  MASP INT IDENTITY PRIMARY KEY,
  TENSP NVARCHAR(60),
  SOLUONGTONKHO INT DEFAULT (0),
  GIA INT ,
  GIABAN INT,
  LOAI NVARCHAR(15) CHECK (LOAI IN ('DIEN THOAI','LINH KIEN')),
  DUONGDAN_HINHANH NVARCHAR(200),
  CHITIET NVARCHAR(500),
  MAHSX INT FOREIGN KEY (MAHSX) REFERENCES HANGSX( MAHSX),
 
)
CREATE TABLE DANHGIA(
  MASP INT PRIMARY KEY,
  TOTAL_SCORE INT DEFAULT(0) , 
  ONE_STAR INT DEFAULT(0) ,
  TOW_STAR INT DEFAULT(0) ,
  THREE_STAR INT DEFAULT(0) ,
  FOUR_STAR INT DEFAULT(0) ,
  FIVE_STAR INT DEFAULT(0) ,
)

CREATE TABLE NHANVIEN(
  MANV INT IDENTITY PRIMARY KEY,
  TENNV NVARCHAR(30),
  CMND NVARCHAR(12),
  QUEQUAN NVARCHAR(30),
  NAMSINH DATE DEFAULT(GETDATE()),
  DIACHITHUONGTRU NVARCHAR(30),
  SDT NVARCHAR (10),
  LUONG INT ,
  NGAYVAOLAM DATE DEFAULT(GETDATE()),
  GIOITINH BIT,
  ANHCHANDUNG NVARCHAR(40),
  CHUCVU NVARCHAR(30) CHECK (CHUCVU IN ('CHU SHOP','QUAN LY','NHAN VIEN MUA BAN','NHAN VIEN SUA CHUA')),
  MATK INT FOREIGN KEY  (MATK) REFERENCES TAIKHOAN(MATAIKHOAN)

)


CREATE TABLE KHACHHANG(
  MAKH INT IDENTITY PRIMARY KEY,
  TENKH NVARCHAR(30),
  SDT NVARCHAR(10)  ,
  DIACHI NVARCHAR(50),
)

CREATE TABLE HOADON(
  MAHD INT IDENTITY PRIMARY KEY,
  MAKH INT FOREIGN KEY (MAKH) REFERENCES KHACHHANG(MAKH) ON DELETE CASCADE ON UPDATE CASCADE,
  MANV INT FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV) ON DELETE CASCADE ON UPDATE CASCADE,
  NGAYLAP DATE DEFAULT(GETDATE()),
  TONGTIEN INT ,
)

---LICH SU SAN PHAM DA XEM CUA KHACH HANG , TOI DA LUU GIU DUOC 12 SAN PHAM DA XEM
CREATE TABLE LICHSUXEM(
    MATAIKHOAN INT FOREIGN KEY (MATAIKHOAN) REFERENCES TAIKHOAN(MATAIKHOAN) ON  DELETE CASCADE  ON UPDATE CASCADE,
    MA_SANPHAM INT FOREIGN KEY (MA_SANPHAM) REFERENCES SANPHAM(MASP), 
	NGAYXEM DATE DEFAULT(GETDATE()),
	CONSTRAINT PK_LICHSUXEM PRIMARY KEY(MATAIKHOAN,MA_SANPHAM,NGAYXEM)
)
---LICH SU DA SAN PHAM DA MUA CUA KHACH HANG , TOI DA LUU GIU DUOC 12 SAN PHAM DA XEM
CREATE TABLE LICHSUMUA(
    MATAIKHOAN INT FOREIGN KEY (MATAIKHOAN) REFERENCES TAIKHOAN(MATAIKHOAN) ON  DELETE CASCADE  ON UPDATE CASCADE,
    MA_SANPHAM INT FOREIGN KEY (MA_SANPHAM) REFERENCES SANPHAM(MASP), 
	NGAYMUA DATE DEFAULT(GETDATE()),
	SOLUONG INT,
    THANHTOAN_XONG BIT,
	THANHTIEN INT,
	CONSTRAINT PK_LICHSUMUA PRIMARY KEY(MATAIKHOAN,MA_SANPHAM,NGAYMUA)
)
CREATE TABLE GIOHANG(
    MATAIKHOAN INT FOREIGN KEY (MATAIKHOAN) REFERENCES TAIKHOAN(MATAIKHOAN) ON  DELETE CASCADE  ON UPDATE CASCADE,
    MA_SANPHAM INT FOREIGN KEY (MA_SANPHAM) REFERENCES SANPHAM(MASP), 
    SOLUONG INT,
    DAMUA BIT DEFAULT (0),
    NGAYMUA DATE DEFAULT(GETDATE()),
   CONSTRAINT PK_GIOHANG PRIMARY KEY(MATAIKHOAN,MA_SANPHAM,NGAYMUA)
)
-- TAI KHOAN LOAI KHACH HANG DUNG DE LUU GIU CAC THONG TIN KHAC CUA KHACH HANG (CON LAI DEU GIONG VOI TAI KHOAN QUAN LY)
CREATE TABLE TAIKHOAN_KHACH(
  MATAIKHOAN INT FOREIGN KEY (MATAIKHOAN) REFERENCES TAIKHOAN(MATAIKHOAN) ON  DELETE CASCADE  ON UPDATE CASCADE,
  LOAI_KHACH NVARCHAR(30) CHECK(LOAI_KHACH IN ('VIP','THUONG','VANG LAI')), -- LOAI KHACH : KHACH MUA SO LUONG SAN PHAM > 50 LA VIP , <50 VA > 20 LA THUONG, < 20 LA VANG LAI, TU DO TA CO CAC CHINH SACH KHUYEN MAI RIENG
  MAKH  INT FOREIGN KEY (MAKH) REFERENCES KHACHHANG(MAKH) ON  DELETE CASCADE  ON UPDATE CASCADE,
  CONSTRAINT PK_TAIKHOANG_KHACH PRIMARY KEY(MATAIKHOAN) 

) 

CREATE TABLE NHANXET_SP (
  MASP INT FOREIGN KEY (MASP) REFERENCES SANPHAM (MASP) ON DELETE CASCADE ON UPDATE CASCADE,
  MATAIKHOAN INT  FOREIGN KEY (MATAIKHOAN) REFERENCES TAIKHOAN( MATAIKHOAN) ON DELETE CASCADE ON UPDATE CASCADE, 
  NOIDUNG NTEXT,
  TITILE NVARCHAR(200),
  LIKE_ INT DEFAULT(0),
  DISLIKE INT DEFAULT(0),
  SCORE INT DEFAULT(0),
  POSTTIME DATETIME DEFAULT(GETDATE()),
  CONSTRAINT PK_NHANXET_SP PRIMARY KEY(MASP, MATAIKHOAN)
)

CREATE TABLE CHITIET_HOADON(
  MAHD INT FOREIGN KEY (MAHD) REFERENCES HOADON (MAHD), 
  MASP INT FOREIGN KEY (MASP) REFERENCES SANPHAM (MASP),
  SOLUONG INT,
  CONSTRAINT PK_CT PRIMARY KEY(MAHD, MASP)
)

CREATE TABLE KHUYENMAI(
  MAKM INT IDENTITY PRIMARY KEY,
  LOAI NVARCHAR(17) CHECK (LOAI IN ('GIAM GIA', 'SALE KHUNG'))
)
CREATE TABLE KHUYENMAI_GIAMGIA(
  MAKM INT FOREIGN KEY (MAKM) REFERENCES KHUYENMAI(MAKM) ON DELETE CASCADE,
  MASP INT FOREIGN KEY (MASP) REFERENCES SANPHAM(MASP) ON DELETE CASCADE, 
  PHANTRAM INT ,
  CONSTRAINT PK_TANGQUA PRIMARY KEY (MAKM)
